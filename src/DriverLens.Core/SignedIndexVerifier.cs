using System;
using System.Security.Cryptography;
using System.Text;

namespace DriverLens.Core;

public static class SignedIndexVerifier
{
    // Real public key bytes pasted from the generated keypair
    public static readonly byte[] TrustedPublicKey = { 
        0x30, 0x59, 0x30, 0x13, 0x06, 0x07, 0x2A, 0x86, 0x48, 0xCE, 0x3D, 0x02, 0x01, 0x06, 0x08, 0x2A, 
        0x86, 0x48, 0xCE, 0x3D, 0x03, 0x01, 0x07, 0x03, 0x42, 0x00, 0x04, 0x3B, 0x16, 0xF0, 0xF1, 0x57, 
        0x55, 0x7C, 0xA1, 0x9A, 0xF9, 0x21, 0xF3, 0x7E, 0x75, 0x38, 0x3C, 0xF0, 0x86, 0xD7, 0x34, 0xC2, 
        0x30, 0x0F, 0x5B, 0x3F, 0x4D, 0x21, 0x57, 0xF9, 0xD7, 0x2B, 0xE5, 0x39, 0xE3, 0x57, 0x3A, 0xEF, 
        0x44, 0xB0, 0x18, 0x29, 0xCB, 0x74, 0x8D, 0xC6, 0x3E, 0x52, 0x03, 0xB3, 0x96, 0xB1, 0xBE, 0x53, 
        0xAB, 0x38, 0xB3, 0x0D, 0xCF, 0x86, 0x93, 0x5C, 0xAF, 0x69, 0x78 
    };

    public static bool Verify(string payloadJson, string base64Signature, byte[]? publicKey = null)
    {
        try
        {
            var keyToUse = publicKey ?? TrustedPublicKey;
            if (keyToUse == null || keyToUse.Length == 0)
            {
                return false;
            }

            using var ecdsa = ECDsa.Create();
            ecdsa.ImportSubjectPublicKeyInfo(keyToUse, out _);
            var bytes = Encoding.UTF8.GetBytes(payloadJson);
            var sig = Convert.FromBase64String(base64Signature);
            return ecdsa.VerifyData(bytes, sig, HashAlgorithmName.SHA256);
        }
        catch
        {
            return false; // any malformed input/signature/key fails closed
        }
    }
}
